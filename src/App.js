const [loading, setLoading] = React.useState(false);
const [lecSchedulePayload, setLecSchedulePayload] = useState([]); // Initialize as an empty array
const [coursePayload, setcoursePayload] = useState(''); 
const [iscourseClosed, setiscourseClosed] = useState(false);
const [responseMessage, setResponseMessage] = useState('');
const [continueJob, setContinueJob] = useState(false); 
const [iscourseOffered, setiscourseOffered] = useState(false);


  // Toggle the navigation menu
  const toggleMenu = () => setIsMenuOpen((prev) => !prev);


  // Initialize autocomplete functionality
  const initAutocomplete = () => {
    $('#searchInput').on('input', function () {
      const query = $(this).val().toLowerCase();
      $('#suggestionList').empty().hide();

      if (query) {
        const filteredSuggestions = suggestions.filter((item) =>
          item.toLowerCase().includes(query)
        );

        filteredSuggestions.forEach((item) => {
          $('#suggestionList').append(
            `<li class="list-group-item">${item}</li>`
          );
        });

        if (filteredSuggestions.length > 0) {
          $('#suggestionList').show();
        }
      }
    });

    $('#suggestionList').on('click', 'li', function () {
      $('#searchInput').val($(this).text());
      $('#suggestionList').empty().hide();
      setInputValue($(this).text());
    });

    return () => {
      $('#searchInput').off('input');
      $('#suggestionList').off('click');
    };
  };

  useEffect(initAutocomplete, [suggestions]);

  //submit form fucntion
const submitForm = (e) => {
    e.preventDefault();
  
    setLoading(true); // Show the loading modal

    // Make a POST request to the backend
    let corseCode = inputValue.replace(' ','-') ;
    axios
      .post('https://coursehawk-expressjs-mongodb-125-dkchcwcwcncnbbb2.canadacentral-01.azurewebsites.net/payload', {input:corseCode} )
      .then((response) => {
        setLoading(false); // Hide the loading modal

        let lecSchecduleJSON=JSON.stringify(response.data);
        let lecSchecdulePayload=JSON.parse(lecSchecduleJSON);
        let lecSchedulePayload=lecSchecdulePayload.x;
        let coursePayload=lecSchecdulePayload.y;
        
        let lecOpenOrClosed = lecSchedulePayload.map((line, index) => {
        return {
          course: line,
          isClosed: line.includes('Closed'), // Check if the line contains 'Closed'
          isOffered: !line.includes('Course not currently offered (Closed)')
        };
      });

      setLecSchedulePayload(lecOpenOrClosed);
      setcoursePayload("  "+coursePayload);

      // Check if all courses are closed/offered
      const allCoursesClosed = lecOpenOrClosed.every((lec) => lec.isClosed);
      const iscourseOffered = lecOpenOrClosed.every((lec) => lec.isOffered);

      // Set iscourseClosed based on whether all courses are closed
      setiscourseClosed(allCoursesClosed);
      setiscourseOffered(iscourseOffered);

      })
      .catch((error) => {
        setLoading(false); // Hide the loading modal
        console.error('Error making API call:', error);
      });
  };


  // Set interval for API call every hour for 24 hours after submission
  useEffect(() => {
    let interval;
    let timeout;

    if (iscourseClosed && iscourseOffered) {
      // Start the API call every hour
      interval = setInterval(() => {
        submitForm({ preventDefault: () => {} }); // Call submitForm without the event
      }, 1000 * 60 * 60); // 1 hour in milliseconds

      // Stop the interval after 24 hours
      timeout = setTimeout(() => {
        clearInterval(interval);
        console.log('Finished 24 hours of API calls.');
              if (iscourseClosed) {
        setContinueJob(true); // Set continueJob to true when the task is done
      }
      }, 1000 * 60 * 60 * 24); // 24 hours in milliseconds
    }

    // Cleanup on component unmount or when isSubmitted changes
    return () => {
      clearInterval(interval);
      clearTimeout(timeout);
    };
  }, [iscourseClosed]); // This hook runs when isSubmitted changes




  // Render the navigation bar
  const renderNavBar = () => (
    <nav className='sticky-top navbar-light bg-light'>
      <div className="container">
        <a href="#" className="brand">
          CourseHawk - McMaster University 
        </a>
        <button
          className="toggle-button"
          aria-label="Toggle navigation"
          onClick={toggleMenu}
        >
          <span className="toggle-icon"></span>
          <span className="toggle-icon"></span>
          <span className="toggle-icon"></span>
        </button>
        <div className={`menu ${isMenuOpen ? 'active' : ''}`}>
          <ul className="nav-links">
            <li>
              <a href="#">Help</a>
            </li>
          </ul>
        </div>
      </div>
    </nav>
  );



